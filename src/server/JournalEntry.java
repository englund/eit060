package server;

public class JournalEntry {
	private String date, content, signature;

	/**
	 * @param date
	 * @param content
	 * @param signature
	 */
	public JournalEntry(String date, String content, String signature) {
		this.date = date;
		this.content = content;
		this.signature = signature;
	}
	
	

}
